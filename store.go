package main

import (
	"database/sql"
	"log"
	"os"

	_ "github.com/mattn/go-sqlite3"
)

func setupStore() {
	if _, err := os.Stat("job.store"); err == nil {
		return
	}

	db, err := sql.Open("sqlite3", "job.store")
	if err != nil {
		panic(err)
	}
	defer db.Close()

	sqlStmt := `
	CREATE TABLE meta (id INTEGER NOT NULL PRIMARY KEY, name TEXT);
	CREATE TABLE queue (name TEXT PRIMARY KEY);
	CREATE TABLE tasks (id TEXT PRIMARY KEY, queue TEXT,
						name TEXT,
						priority INT,
						created_at DATETIME,
						updated_at TIMESTAMP);
	CREATE TABLE jobs (id TEXT PRIMARY KEY, queue TEXT,
						name TEXT,
						priority INT,
						created_at DATETIME,
						updated_at TIMESTAMP);
	`
	_, err = db.Exec(sqlStmt)
	if err != nil {
		log.Printf("%q: %s\n", err, sqlStmt)
		return
	}
}

